package d25collections;

public class my {
    /*

    Java'daki Collection Framework hiyerarşisi, veri yapılarının
    nasıl organize edildiğini ve
    birbirleriyle nasıl ilişkili olduğunu gösterir.

    Temel yapı şöyle:

    Iterable: Tüm koleksiyonların üstünde bulunan, döngülerde (loop)
     kullanılmayı sağlayan temel bir yapı.

            Collection: List, Queue ve Set gibi veri yapılarının yer
            aldığı büyük grup.

    List: Sıralı bir veri yapısıdır. Elemanlara sırayla erişebilirsin.

            ArrayList: Dinamik bir dizi gibidir. Elemanlara hızlı erişi
             sağlar.
    LinkedList: Elemanlar birbirine bağlıdır. Ekleme ve silme işlemleri
     hızlıdır.
            Vector: Eski bir yapı, ArrayList'e benzer ama daha yavaş
             çalışır.
    Stack: Son giren ilk çıkar mantığıyla çalışır (LIFO - Last In First
     Out).
    Queue: Kuyruk yapısıdır, ilk giren ilk çıkar (FIFO - First In First
     Out) mantığına dayanır.

    PriorityQueue: Elemanları belirli bir öncelik sırasına göre işler.
            Deque: Çift uçlu kuyruk, yani hem baştan hem sondan eleman
            ekleyip çıkarabilirsin.
    ArrayDeque: Deque'nin bir implementasyonudur.
    Set: Benzersiz elemanlar içerir, aynı elemandan iki tane olamaz.

            HashSet: Elemanlar sırasızdır ve benzersizdir.
    LinkedHashSet: Elemanlar eklenme sırasına göre saklanır.
            SortedSet: Elemanları sıralı tutan bir set yapısı.

    TreeSet: Elemanları doğal bir sıraya göre saklar.
    Map: Anahtar-değer çiftleriyle çalışan bir veri yapısıdır. Her
     anahtar sadece bir değeri işaret eder.

            HashMap: Anahtar-değer çiftlerini sırasız şekilde saklar.

            LinkedHashMap: Anahtar-değer çiftlerini eklenme sırasına
             göre tutar.

    Hashtable: HashMap’e benzer, fakat eski ve thread-safe bir yapıdır.

            SortedMap: Anahtar-değer çiftlerini sıralı tutar.

    TreeMap: Anahtarları doğal bir sıraya göre saklar.
    Bu yapıda, arayüzler (interface) ve sınıflar (class) yer alır.
    Arayüzler, veri yapılarına genel bir
    çerçeve sunarken, sınıflar bu yapıları uygulayan (implement eden)
     gerçek verilerdir.
    */
}
